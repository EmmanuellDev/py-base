# DATATYPES AND THEIR DECLARATIONS

# Text Type (str)
text_var = "hello"
print (text_var)
print(type(text_var))


# Numeric Type (int, float, comp)
#int
int_var = 10
print (int_var)
print(type(int_var))

#float
float_var = 10.5
print (float_var)
print(type(float_var))

#comp
comp_var = 2 + 3j
print (comp_var)
print(type(comp_var))


# Sequence Type (List, Tuple, Range)
#list
list_var = [2, 4, 6, 8, 10]
print (list_var)
print(type(list_var))

#tuple
tuple_var = (2, 4, 6, 8)
print (tuple_var)
print(type(tuple_var))

#range
range_var = range(10)
print (range_var)
print(type(range_var))


# Mapping Type (dictionary)
dict_var = {"name" : "emman", "age" : "18"}
print (dict_var)
print(type(dict_var))


# Set Type (set, frozenset)
#set
set_var = {2,4,6,8,10}
print (set_var)
print(type(set_var))

#frozenset
fro_var = frozenset([2,4])
print (fro_var)
print(type(fro_var))


# Boolean Type (Boolean)
#Bool
bool_var = True
print (bool_var)
print(type(bool_var))

